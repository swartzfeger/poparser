const SORTED_ITEM_KEYS = Object.keys(ITEM_PRICES).sort((a, b) => b.length - a.length);
const SORTED_DESC_KEYS = Object.keys(ITEM_DESCRIPTIONS).sort((a, b) => b.length - a.length);

export function getItemDescription(sku: string): string {
  if (!sku) return "";
  const normalizedSku = sku.trim().toUpperCase();

  // Try exact match
  if (ITEM_DESCRIPTIONS[normalizedSku]) {
    return ITEM_DESCRIPTIONS[normalizedSku];
  }

  // Try prefix matching
  for (const key of SORTED_DESC_KEYS) {
    if (normalizedSku.startsWith(key)) {
      return ITEM_DESCRIPTIONS[key];
    }
  }

  return "";
}

export function getItemPrice(sku: string, priceLevel: string | undefined): number {
  if (!sku || !priceLevel) return 0;
  
  const normalizedSku = sku.trim().toUpperCase();
  const normalizedLevel = priceLevel.trim();

  // Try exact match on SKU
  if (ITEM_PRICES[normalizedSku]) {
    return ITEM_PRICES[normalizedSku][normalizedLevel] || 0;
  }

  // Try prefix matching
  for (const key of SORTED_ITEM_KEYS) {
    if (normalizedSku.startsWith(key)) {
      return ITEM_PRICES[key][normalizedLevel] || 0;
    }
  }

  return 0;
}
